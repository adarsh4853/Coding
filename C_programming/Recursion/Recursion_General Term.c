#include<stdio.h>

int term(int n)
{
	if(n==1 || n==2) return 1;
	else return 6*term(n-2)-term(n-1);
}

int main()
{
	int n;
	printf("Enter n: ");
	scanf("%d",&n);
	
	printf("%d\n",term(n));
	return 0;
}

